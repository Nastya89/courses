public class Practice {
    public static void main(String[] args) {
        boolean b1 = true;
        char c1 = 'b';
        int x1 = 878;
        int i1 = 0b101;
        int six = 06;
        int a1 = 0X0005;
        float f1 = 78.45f;
        double d4 = 654.321;
        String str1 = "MyProgram";
        System.out.println("Логический литерал = " + b1);
        System.out.println("Строковый = " + str1);
        System.out.println("Символьный  = " + c1);
        System.out.println("Целочисленный 2-й = " + i1);
        System.out.println("Целочисленный 8-й = " + six);
        System.out.println("Целочисленный 10-й = " + x1);
        System.out.println("Целочисленный 16-й = " + a1);
        System.out.println("Литерал типа float = " + f1);
        System.out.println("Литерал типа double = " + d4);
    }
}
